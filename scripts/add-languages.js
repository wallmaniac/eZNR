// Find the last `},` that closes the `hr` block, then insert after it
const insertPoint = content.lastIndexOf('  },\r\n};');
if (insertPoint === -1) {
  // Try LF-only
  const insertPointLF = content.lastIndexOf('  },\n};');
  if (insertPointLF === -1) {
    console.error('Could not find insertion point');
    process.exit(1);
  }
  const before = content.substring(0, insertPointLF + 4);
  const after = content.substring(insertPointLF + 4);
  content = before + deBlock + slBlock + srBlock + after;
} else {
  const before = content.substring(0, insertPoint + 5); // includes `  },\r`
  const after = content.substring(insertPoint + 5);      // `\n};` onwards
  content = before + deBlock + slBlock + srBlock + after;
}


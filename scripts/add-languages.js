// Build the new language blocks as strings
function objToJsString(obj, langKey) {
  let lines = [`\n  ${langKey}: {`];
  for (const [key, val] of Object.entries(obj)) {
    const escaped = val.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
    lines.push(`    ${key}: '${escaped}',`);
  }
  lines.push('  },');
  return lines.join('\n');
}

